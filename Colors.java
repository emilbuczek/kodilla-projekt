
public class Colors {
    public static void main(String[] args){
        KodillaColors myColors = KodillaColors.G;
        switch(myColors){
            case B:
                System.out.println("Blue");
                break;
            case Y:
                System.out.println("Yellow");
                break;
            case G:
                System.out.println("Green");
                break;
            case P:
                System.out.println("Pink");
                break;

        }
    }
}
